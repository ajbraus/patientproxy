<h4>How it works:</h4>
<div class="actionsteps">
	
	<div class="first">
	<img src="images/" title="" alt="" width="200px" height="200px" />
	<p>First, walk through the Patient Proxy wizard to build your health proxy and living will.</p>
	</div>
	
	<div class="middle">
	<img src="images/" title="" alt="" width="200px" height="200px" />
	<p>After filling out your proxy:</p>
	<ul>
		<li>Email the unsigned proxy to anyone you wish - your proxy, your doctor, or to yourself.</li>
		<li>Save a copy to your computer's hard drive.</li>
		<li>Print out a copy of your Patient Proxy to sign.</li>
	</ul>
	</div>
	
	<div class="last">
	<img src="images/" title="" alt="" width="200px" height="200px" />
	<p>The last step is the most important. Sign your proxy in the presence of two witnesses* and submit this to your doctor and to hospitals where you receive care.</p>
	<p>If you have a scanner, scan a signed copy for your own records and email the signed copy to caregivers, family and friends.</p>
	<p>* <span class="sidenote">The seal of a notary public is required for proxies in West Virginia, North Carolina and Virginia.</span></p>
	</div>

